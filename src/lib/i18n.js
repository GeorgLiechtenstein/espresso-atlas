/** Convenience: get translations for a given lang */
export function t(lang) {
  return tr[lang] ?? tr.de;
}

/** Score label as pure function (used by non-hook contexts) */
export function scoreLabel(score, lang = 'de') {
  if (score === null || score === undefined) return null;
  const n = parseFloat(score);
  const labels = tr[lang] ?? tr.de;
  if (n <= 4)  return labels.scoreBad;
  if (n < 7)   return labels.scoreMediocre;
  if (n <= 8)  return labels.scoreGood;
  return labels.scoreExcellent;
}
